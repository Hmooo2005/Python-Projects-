age = int(input("write your age: "))
months_age = age*12
weeks_age = months_age*4
days_age = weeks_age*7
hours_age = days_age*24
minutes_age = hours_age*60
seconds_age = minutes_age*60
print(f"Your Age in Months Is: {months_age} Month")
print(f"Your Age in Weeks Is: {weeks_age} Week")
print(f"Your Age in Days Is: {days_age} Day")
print(f"Your Age in Hours Is: {hours_age} Hour")
print(f"Your Age in Minutes Is: {minutes_age} Minutes")
print(f"Your Age in Seconds Is: {seconds_age} Second")
