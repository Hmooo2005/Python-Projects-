print(name.lower()) #print variable value in lower case

print(name.upper()) #print variable value in upper case

print(name.replace('a','m')) #replace character in string by the written character

print(name.swapcase()) #reverse the letters from capital to small or small to capital

print(name.strip('M')) #remove character from right and left

print(name.lstrip('M')) #remove character from left only

print(name.rstrip()) #remove character from right only

print(name.title()) #make letters at begining of word and after numbers in capital form

print(name.zfill(3)) #add zero befor number

print(name.split()) #split every word alone and but it in list

print(name.rsplit()) #split every word alone and but it in list start from right

print(name.splitlines()) #split string with many lines and but it in a list by make every line an element

